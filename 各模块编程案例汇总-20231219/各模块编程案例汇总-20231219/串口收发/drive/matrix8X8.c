void matrix8X8(u8 font[])//显示8*8点阵font[8]、阴码、低位在前、C51
{				
	u8 i,line;
	line=0xFE;
	for(i=0;i<8;i++)//逐行显示8行点阵font
	{	
		COL=line;
		COD=font[i];
		line=_crol_(line,1);
		delay_ms(3);
	}
}